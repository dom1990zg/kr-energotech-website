window.setLang = function(l) {
  if (!T[l]) return;
  lang = l;
  var t = T[l];
  var rtl = (l === 'ar');
  document.documentElement.lang = l;
  document.documentElement.dir = rtl ? 'rtl' : 'ltr';
  document.body.dir = rtl ? 'rtl' : 'ltr';
  var ht = document.getElementById('heroTitle');
  if (ht) ht.innerHTML = heroTitles[l] || heroTitles['en'];
  var els = document.querySelectorAll('[data-i18n]');
  for (var i = 0; i < els.length; i++) {
    var el = els[i], k = el.getAttribute('data-i18n');
    if (t[k] !== undefined) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') el.placeholder = t[k];
      else if (el.tagName === 'OPTION' || el.tagName === 'BUTTON') el.textContent = t[k];
      else el.innerHTML = t[k];
    }
  }
  var btns = document.querySelectorAll('.lang-btn');
  for (var j = 0; j < btns.length; j++) {
    btns[j].classList.toggle('active', btns[j].textContent === l.toUpperCase());
  }
};

